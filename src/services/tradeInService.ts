import { supabase } from '../integrations/supabase/client';

export async function deleteTradeIn(tradeInId: string) {
  console.log(`ğŸ—‘ï¸ Starting deletion of trade-in ${tradeInId}`);
  
  try {
    // First verify the trade-in exists
    const { data: existing, error: checkError } = await supabase
      .from('trade_ins')
      .select('id, customer_id')
      .eq('id', tradeInId)
      .single();
    
    if (checkError) {
      console.error('âŒ Error checking trade-in:', checkError);
      throw checkError;
    }
    
    if (!existing) {
      console.error('âŒ Trade-in not found:', tradeInId);
      throw new Error('Trade-in not found');
    }

    console.log('ğŸ“ Found trade-in to delete:', existing);

    // Delete the trade-in (items will be deleted via CASCADE)
    const { data, error } = await supabase
      .from('trade_ins')
      .delete()
      .eq('id', tradeInId)
      .select();

    if (error) {
      console.error('âŒ Failed to delete trade-in:', error);
      throw error;
    }
    
    console.log('âœ… Trade-in deleted successfully:', {
      deletedId: tradeInId,
      response: data
    });

    return data;
  } catch (error) {
    console.error('âŒ Error in deleteTradeIn:', error);
    throw error;
  }
}